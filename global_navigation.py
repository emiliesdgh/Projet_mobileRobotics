# Function to create empty map (just visual)

def create_empty_plot(cols, rows):
    """
    Helper function to create a figure of the desired dimensions & grid
    
    :param max_val: dimension of the map along the x and y dimensions
    :return: the fig and ax objects.
    """
    fig, ax = plt.subplots(figsize=(7,7))
    ax.invert_yaxis()
    
    # Set max_val to the highest value to print the ticks
    max_val = rows if rows>=cols else cols
    
    major_ticks = np.arange(0, max_val+1, 5)
    minor_ticks = np.arange(0, max_val+1, 1)
    ax.set_xticks(major_ticks)
    ax.set_xticks(minor_ticks, minor=True)
    ax.set_yticks(major_ticks)
    ax.set_yticks(minor_ticks, minor=True)
    ax.grid(which='minor', alpha=0.2)
    ax.grid(which='major', alpha=0.5)
    ax.set_ylim([rows,-1])
    ax.set_xlim([-1,cols])
    ax.grid(True)
    
    return fig, ax

